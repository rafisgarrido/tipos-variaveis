public class OperadoresRepeticao {
    
    public static void main (String[] args){
        int numero = 5;
        boolean variavel = true;

        //x repeticao
        numero = numero + 2;

        numero = numero + 1;

        numero++;

        System.out.println(numero);

        System.out.println(numero --);

        System.out.println(numero);

        System.out.println(!variavel);

        variavel = !variavel;

        System.out.println(variavel);

    }
}
